export type TranslationKey = keyof typeof dictionary.en;

export function t(language: LanguageCode, key: TranslationKey, vars?: Record<string, string | number>) {
  let text: string = dictionary[language][key] ?? dictionary.en[key];
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replace(`{${name}}`, String(value));
    }
  }
  return text;
}
